function [A1,A2,A3,A4]=calA(num,p1,per1,p2,per2,p3,per3,p4,per4)
%如何计算三个一级指标的评分：当14个二级指标x是行向量时,
% 分成x1,x2,x3
x1=num(:,1:4);
x2=num(:,5:9);
x3=num(:,10:14);
x4=num(:,15:18);
z1=x1*p1;
z2=x2*p2;
z3=x3*p3;
z4=x4*p4;
A1=[per1(1)*z1(:,1)/100+per1(2)*z1(:,2)/100+per1(3)*z1(:,3)/100];
A2=[per2(1)*z2(:,1)/100+per2(2)*z2(:,2)/100+per2(3)*z2(:,3)/100];
A3=[per3(1)*z3(:,1)/100+per3(2)*z3(:,2)/100+per3(3)*z3(:,3)/100];
A4=[per4(1)*z4(:,1)/100+per4(2)*z4(:,2)/100+per4(3)*z4(:,3)/100];