function [c,ceq,k1,k2,s]=seminfcon(x,s)
%c定义了非线性不等式约束
%ceq定义了非线性等式约束
%k1，k2定义了半无穷约束
%s定义了取样步长
c=[];
ceq=[];
if isnan(s(1,1))
    s=[0.2 0;0.2 0];
end
%定义取样步长
w1=1:s(1,1):100;
w2=1:s(2,1):100;
%半无穷约束
k1=sin(x(1)*w1).*cos(x(2)*w1)-1/1000*(w1-50).^2-sin(w1*x(3))-x(3)-1;
k2=sin(x(2)*w2).*cos(x(1)*w2)-1/1000*(w2-50).^2-sin(w2*x(3))-x(3)-1;
%画出半无穷约束的图像
plot(w1,k1,'b-',w2,k2,'r:');